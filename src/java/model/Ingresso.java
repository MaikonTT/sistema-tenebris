package model;

/**
 *
 * @author Takeshi
 */
public class Ingresso {
    private int id;
    private String codigo;
    private String data;
    private double valor;
    private String cpf;

    public Ingresso() {
    }

    public Ingresso(int id, String codigo, String data, double valor, String cpf) {
        this.id = id;
        this.codigo = codigo;
        this.data = data;
        this.valor = valor;
        this.cpf = cpf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

}
